namespace Bluegrams.Calcex.Parsing
{
    /// <summary>
    /// Enumeration of possible decimal separator styles.
    /// </summary>
    public enum SeparatorStyle
    {
        /// <summary>
        /// Uses dot ('.') as decimal separator and comma (',') as argument separator.
        /// </summary>
        Dot = '.',
        /// <summary>
        /// Uses comma (',') as decimal separator and semicolon (';') as argument separator.
        /// </summary>
        Comma = ','
    }
}
